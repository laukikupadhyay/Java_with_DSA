package com.Basics;
// public class should have the same name as the Java File
public class Hello {
    public static void main(String[] args) {
        // main function is the entry point of a java program
        // public: the main function should be should so that it can be publicly accessed
        // static: the main function uses static data so that it can run without creating an object of the function
        // void: return type function which returns null value after execution.
        // String[] args: Command line arguments. It is an array that stores collection string values.
        System.out.println("Hello World!");
        System.out.println("8847468404" );
    }
}
